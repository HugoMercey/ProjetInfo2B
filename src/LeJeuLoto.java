    private void PlaceJ(){
        //Fonction servant à afficher les noms des Joueurs et leur couleur sur les boutons
        for(int i=0; i<lstJ.getNbJoueur(); i++){
            javax.swing.JButton comp = (javax.swing.JButton) jPanel3.getComponent(i);
            comp.setText(lstJ.getJoueur(i).getPseudo());
            comp.setBackground(lstJ.getJoueur(i).getCouleur());
        }
    }
    
