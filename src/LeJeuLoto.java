    private void jMenuItemAjoutJoueurActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemAjoutJoueurActionPerformed
        // TODO add your handling code here:
        if(lstJ.getNbJoueur()<4){ //S'il y a moins de 4 joueurs déjà créé:
            SaisieJoueurDlg diag = new SaisieJoueurDlg(this, true);
            diag.setVisible(true);
            if(diag.getOk()){
                lstJ.ajoutJoueur(diag.getJoueur()); //Ajout du joueur créé dans la fenêtre SaisieJoueurDlg dans la liste servant à les gérér
            }
            PlaceJ(); //Affichage des Joueurs sur les boutons
        }else{ //S'il y'a 4 joueurs ou plus, ne rien faire
            jLabel2.setText("Il y a trop de joueurs");
        }
    }//GEN-LAST:event_jMenuItemAjoutJoueurActionPerformed

